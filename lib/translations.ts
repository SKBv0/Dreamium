export type Language = keyof typeof translations

import { fixTurkishMojibake } from './text-normalize'

export function getTranslation(lang: Language, key: string): string {
  const normalize = (val: any): string => {
    const s = (val as unknown as string) ?? ''
    return lang === 'tr' ? fixTurkishMojibake(s) : s
  }
  const keys = key.split('.');
  let result: Record<string, any> = translations[lang] || translations.en;

  for (const k of keys) {
    result = result?.[k];
    if (result === undefined) {
      // Fallback to English if key not found in the current language
      let fallbackResult: Record<string, any> = translations.en;
      for (const fk of keys) {
        fallbackResult = fallbackResult?.[fk];
        if (fallbackResult === undefined) {
          const lastKey = keys[keys.length - 1];
          if (!lastKey) return key;
          // Format to be more readable: replace underscores and capitalize
          return lastKey.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
        }
      }
      return normalize(fallbackResult) || key;
    }
  }

  return normalize(result) || key;
}

