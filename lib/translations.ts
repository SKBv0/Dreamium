export function translateThemeName(themeName: string, language: Language): string {
  // First try to get translation from themes.labels
  const translation = getTranslation(language, `themes.labels.${themeName.toLowerCase()}`);
  if (translation !== `themes.labels.${themeName.toLowerCase()}`) {
    return translation;
  }
  
  // If no translation found, return the original name
  return themeName;
}

